                     <h3 class="header-ribbon">Job description / Role</h3>
                     <p>The Sales Associate is responsible for serving customers on the shop floor and answering their needs in a timely manner in order to ensure a high level of customer satisfaction and achieve optimum sales results.<br />
                        <br />
                        Key Accountabilities <br />
                        •	Greet customers, offer assistance and serve them in order to ensure their needs are answered in a timely manner and in compliance with quality and customer service standards<br />
                        •	Assist customers in their purchase decisions by helping them select relevant and appropriate products, offering proper advice and suggesting gift vouchers when applicable<br />
                        •	Arrange and replenish on a continuous basis the shop shelves and ensure that the shop floor and shelves are kept clean and tidy at all times<br />
                        •	Watch for and prevent security risks and thefts and escalate occurring incidents to the hierarchy in a timely manner in order to avert loss<br />
                        •	Perform physical and electronic inventory of shop products on a regular basis as per Company guidelines in order to ensure accurate stock keeping<br />
                        •	Specific for Fashion: Arrange and display received merchandise under the direction of the<br />
                        •	Merchandiser/Shop Manager; change tag prices of products during promotion periods or as needed<br />
                        •	Specific for Fashion: Take measurements for any needed alteration, assign price according to set price list, fill the alteration book, and notify tailor of the amendments<br />
                        •	Specific for Multimedia: Up-sell and cross-sell products and services in order to reach hourly and daily sales targets Specific for Multimedia: Inform Department Manager of unavailable and nonperforming products<br />
                        •	Specific for Multimedia: Merchandize products under the appropriate sections according to sales trends, date of release, and as per the Department Manager recommendations.
